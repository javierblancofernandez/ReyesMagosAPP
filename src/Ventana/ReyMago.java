    private ImageIcon helperImagen(Reyes rex){
        ImageIcon iconRedimensionado = null;
        try{
        InputStream is = base.buscarFotoRey(rex);
        BufferedImage bi = ImageIO.read(is);
        ImageIcon imagenProducto = new ImageIcon(bi);

        //jLabel1.setIcon(imagenProducto);
        // Redimension de imagen para ajustarla al tamano del JLabel 
        Image imgProd = imagenProducto.getImage();
        jLabel1.setSize(100, 100);
        jLabel2.setSize(300, 300);
        jLabel3.setSize(200, 200);
        int anchoEtiqueta = jLabel1.getWidth();//jLabel1.getWidth();Obtiene el ancho del a imagen
        int anchoEtiquetaa = jLabel1.getHeight();//jLabel1.getHeight();Obtiene el alto de la imagen

        //Se crea un nuevo objeto Image con la imagen redimensionada
        Image imgRedimensionada = imgProd.getScaledInstance(anchoEtiqueta, anchoEtiquetaa, Image.SCALE_DEFAULT);

        // se crea un nuevo objeto ImageIcon a partir de la imagen redimensionada 
         iconRedimensionado = new ImageIcon(imgRedimensionada);

        
        }catch(IOException ex){
             ex.printStackTrace();
        }

        
        return iconRedimensionado;
    }
